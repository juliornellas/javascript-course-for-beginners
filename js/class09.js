/**
 * Typeof
 * Operator to find the data type of a JavaScript variable
 */

 console.log(typeof "Tiradentes")                   // Returns "string"
 console.log(typeof 3.14)                           // Returns "number"
 console.log(typeof true)                          // Returns "boolean"
 console.log(typeof [1,2,3,4])                      // Returns "object"
 console.log(typeof {name:'Tiradentes', age:45})    // Returns "object"
 console.log(typeof new Date())                     // Returns "object"
 console.log(typeof function () {})                 // Returns "function"
 console.log(typeof fruit)                          // Returns "undefined"